package com.swapping2numbers;

public class Square {
	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			for(int j=1;j<=10;j++) {
				if(j<=7) {
					System.out.println("*");
					
					
				}
				else {
					System.out.println("  ");
					
				}
				System.out.println();
				
				
			}
		}
	}

}
